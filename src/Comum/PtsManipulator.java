package Comum;

public class PtsManipulator {
    
    public static int add(int _pts){
        return _pts++;
    }

    public static int rmv1(int _pts){
        return _pts--;
    }

    public static int rmv(int _pts){
        return _pts - 3;
    }

    public static int rmv5(int _pts){
        return _pts - 5;
    }

    public static int win(int _pts){
        return _pts + 5;
    }

}
